package study.tv;

public class LgTV {
    public void turnOn(){
        System.out.println("엘지 티비 - 전원 킴");
    }

    public void turnOff(){
        System.out.println("엘지 티비 - 전원 끔");
    }

    public void volumeUp(){
        System.out.println("엘지 티비 - 소리 높임");
    }

    public void volumeDown(){
        System.out.println("엘지 티비 - 소리 줄임");
    }
}
